package Projeto.Rpg;

public class LocationMap {
    private int id;
    private String name;
    private int zoneCount;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public int getZoneCount() {
        return zoneCount;
    }

    public void setZoneCount(int zone) {
        this.zoneCount = zone;
    }
}
